a = 10 
b = 20

# arithmatic operator
print(a + b)  # + is used for addition
print(a - b)  # - is used for subtraction
print(a * b)  # * is used for multiplication
print(a / b)  # / is used for division
print(a % b)  # % is used for remainder
print(a ** b) # ** is used for power

# comparison operator
print(a <= b) # <= is used for less than or equal to
print(a >= b) # >= is used for greater than or equal to
print(a < b)  # < is used for less than
print(a > b)  # > is used for greater than
print(a == b) # == is used for equal to
print(a != b) # != is used for not equal to
print(a is b) # is used for checking identity
print(a is not b) # is not used for checking identity

# bitwise operator
print(a & b) # & is used for bitwise AND
print(a | b) # | is used for bitwise OR
print(a ^ b) # ^ is used for bitwise XOR
print(~a) # ~ is used for bitwise NOT
print(a << b) # << is used for bitwise left shift
print(a >> b) # >> is used for bitwise right shift
